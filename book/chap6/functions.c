int sum(int a, int b) {
    int return_value;
    return_value = a + b;

    return return_value;
}

int sub(int a, int b) {
    int return_value;
    return_value = a - b;

    return return_value;
}

int mul(int a, int b) {
    int return_value;
    return_value = a * b;

    return return_value;
}

int div(int a, int b) {
    int return_value;
    return_value = a / b;

    return return_value;
}
